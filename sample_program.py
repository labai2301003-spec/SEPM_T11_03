# sample_program.py
# Simple Python program demonstrating basic concepts

def greet(name):
    return f"Hello, {name}!"

def add(a, b):
    return a + b

if __name__ == "__main__":
    name = "GitHub User"
    print(greet(name))

    x, y = 5, 3
    print(f"Sum of {x} and {y} is:", add(x, y))

    # Loop example
    print("Numbers from 1 to 5:")
    for i in range(1, 6):
        print(i)
print("Git practical completed")
